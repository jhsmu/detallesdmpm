    // Cart array to store added items
    var cart = [];

    // Function to add items to the cart
    function addToCart(itemName, itemPrice) {
        var item = {
            name: itemName,
            price: itemPrice
        };

        // Add the item to the cart array
        cart.push(item);

        // Update the cart display or perform any other necessary actions
        updateCartDisplay();
    }

    // Function to update the cart display (you need to implement this)
    function updateCartDisplay() {
        // Implement this function to update your cart display
        // You can update the cart count and total price, for example
        var cartCount = cart.length;
        var totalPrice = cart.reduce(function (acc, item) {
            return acc + item.price;
        }, 0);

        // Update the cart display elements (you need to add these elements in your HTML)
        document.getElementById('cart-count').innerText = cartCount;
        document.getElementById('cart-total').innerText = '$' + totalPrice.toFixed(2);
    }
